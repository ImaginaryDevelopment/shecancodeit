# plus
a = 1 + 2
print(a) # 3

# minus
a = 4 - 1
print (a) # 3

# multiply
a = 5 * 3
print (a) # 15

# power
a = 2 ** 3
print (a) # 8

# modulo
a = 4 % 3
print (a) # 1
a = 6 % 2
print (a) # 0

# less than
print (3 < 5) # True

# greather than
print (4 > 7) # False

# less than or equal to
print (9 <= 10) # True

# greater than or equal to
print (8 >= 8) # True

# equal to
print (7==7) # True
print (8==9) # False

# not equal to
print (7!=7) # False

# boolean NOT
print (not (7!=7)) # True

# boolean AND (shortcircuit)
print ((8==8) and (7==6)) # False
print ((8==8) and (7==7)) # True

# boolean OR (shortcircuit)
print ((8==8) or (7==6)) # True
print ((8==7) or (7==6)) # False
print ((8==7) or (7==7)) # True
