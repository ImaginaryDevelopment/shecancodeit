# The first character of the identifier must be a
# letter of the alphabet or an underscore (_)
a = 1234
_a = "starts with underscore"

# The rest of the identifier name can consist
# of letters, underscores (_) or digits (0-9)
_abc_12_ = 1.2

# Identifier names are case-sensitive
myvar = 12334
myVar = 39392
